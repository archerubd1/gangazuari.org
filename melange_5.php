    <p>রুকসানা বলল, ‘স্যর, আমি ওকে চিনি। আমাদের পাড়াতেই থাকে। আপনি চাইলে নিয়ে যেতে পারি।’</p>
    <p>যা হোক্‌ সেদিন ক্লাসের শেষে সাইকেল চালিয়ে মুসলমান পাড়ায় ঢুকতে ঢুকতে সন্ধ্যা হয়ে এলো। রুকসানাদের বাড়ি সামনেই। বলাই ছিল। ও’ দাঁড়িয়ে ছিল বাড়ির দোরগোড়াতেই।</p>
    <p>ওখানেই সাইকেলটা রেখে সরু পায়ে চলা কাঁচা পথ ধরে মহল্লার ভিতর দিকে এগিয়ে চললাম।</p>
    <p>‘স্যর, আমি কিন্তু ওদের বাড়িতে ঢুকবো না। ডেকে দিয়েই চলে যাব। কেমন?’</p>
    <p>একটা অতি জীর্ণ কুটীরের সামনে দাঁড়িয়ে ‘ওয়াসিম! ওয়াসিম!’ বলে দু’বার চেঁচিয়ে ডেকেই ফুরুৎ করে সরে পড়ল রুকসানা। আমি ভ্যাবাচাকা খেয়ে দাঁড়িয়ে আছি।</p>
    <p>সন্ধ্যা নেমে আসছে। অন্ধকার হতে চলেছে চারিদিক।</p>
    <p>কালো বোরখা পরা এক বৃদ্ধা বেরিয়ে এলেন, ‘কে? কে? কাকে খুঁজছো বাবা?’</p>
    <p>উনি বোধহয় বাইরে কোথাও বেরোতে যাচ্ছিলেন।</p>
    <p>‘মানে….ইয়ে অসীম, মানে ওয়াসিম কি ঘরে আছে? একবার ডেকে দিন না মাসীমা।’ বললাম।</p>
    <p>‘অ। কিন্তু তার তো বড্ড জ্বর গো। ঐ তো ঘরের মধ্যে কাঁথা মুড়ি দিয়ে শুয়ে আছে। যাও না, ঢুকে যাও না বাঁদিকের দরজাটা দিয়ে। জেগেই তো আছে।’</p>
    <p>‘কে এসেছে, মা?’ ভিতর থেকে জিজ্ঞেস ভেসে এলো। স্বরটা আমার চেনা।</p>
    <p>মাটির উপরে চাটাই পেতে শুয়ে আছে সেই গায়ক। ঘরের মধ্যে কোনো আলো নেই। কোনো ভনিতা না করে ঢুকেই বললাম,</p>
    <blockquote>‘তোমার কাছে ক্ষমা চাইতে এসেছি ভাই ওয়াসিম। গানটা তুমি ঠিকই গাও, গেয়েছিলে। আমরাই ভুল জেনে হেনস্থা করেছি তোমায়। বিজন্‌ ও নিশীথ্‌ উচ্চারণই ঠিক।’</blockquote>
    <p>সেই প্রায়ান্ধকার ঘরে মৃদু মৃদু পরিতৃপ্তির হাসি যেন দেখতে পেলাম ছেলেটির মুখে। বললে, ‘বসুন স্যার। বসুন। কিন্তু কোথায় বসতে দিই বলুন তো আপনাকে? আপনি এই সামান্য কথা বলতে এতো দূরে এসেছেন ভাবতেই সংকোচ হচ্ছে আমার।’</p>
    <p>‘আমি আজ আর বসব না ভাই ওয়াসিম। তোমার জন্যে ছোট্ট একটা উপহার এনেছি।’ বইটি হাতে নিয়ে হাসি মুখে পরম মমতায় তার গায়ে হাত বুলাতে লাগল ছেলেটি।</p>
    <p>এমন সময়ে তার মা এক লম্প হাতে ঘরে এসে ঢুকলেন। বইটি চোখের একদম কাছে এনে দেখতে লাগল। কালো মলাট। হার্ড কভার। ‘একাকী এক কবি’। প্রচ্ছদে গুরুদেবের ছবি রয়েছে। বললাম, ‘শীগগির সুস্থ হয়ে ওঠো। একদিন জমিয়ে বসে তোমার গান শুনব।’</p>
    <p>‘হ্যাঁ হুজুর। ছেলে আমার এবার সেরে উঠবে। কাল রাতে ও’ যখন গাইছিল এই দরবেশ সাহেব তো এসেছিলেন আমাদের এই কুঁড়ে ঘরে। আগেও এসেছেন কত বার! বেটা গাইলে মাঝেমাঝেই শুনতে আসেন উনি। কাল তো হাত বুলিয়ে দিয়ে গেছেন ছেলের শিরে; এবার ছেলে সুস্থ হয়ে উঠবেই। দশাসই চেহারা তাঁর। গায়ে কালো আলখাল্লা। একমুখ সাদা দাড়ি গোঁফ। আর ঠোঁটের কোণে তাঁর কী যে মধুর হাসি গো।’</p>
    <p>ওয়াসিম গুনগুনিয়ে উঠল, ‘মধুর তোমার শেষ যে না পাই’। আর আমি? স্তম্ভিত বেভুল হয়ে বসে আছি এক ‘ম্লেচ্ছে’র কুঁড়েঘরে। কী শুনছি এ’সব? গল্প কথা? বানানো? না, আমার তো আর অত সৌভাগ্য নেই যে দেখব এই ভাঙা কুঁড়ের নিচু দরোজা দিয়ে ঝুঁকে ঢুকছেন কালো আলখাল্লাধারী এক দিব্যপুরুষ! শ্বেতশুভ্র শ্মশ্রুগুম্ফ। সমুন্নত নাসা। উদ্দীপ্ত প্রেমময় চক্ষুদ্বয়। আর এসে হাতটা বাড়িয়ে দিচ্ছেন প্রিয় গায়কের দিকে।</p>
    <p>পরম ভরোসায় সে গাইছে,</p>

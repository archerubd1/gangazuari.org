  <h2 class="mb-4">রবিগান রহস্য -৩- আমি যখন তাঁর দুয়ারে</h2>
  <p><em>– সোমনাথ সরকার</em></p>

  <p>এবার লিখব একটা আপাত সহজবোধ্য গান নিয়ে। পাঠকদের এবং যাঁরা রবীন্দ্রসঙ্গীতের ভক্ত তাঁদের আমার বক্তব্য বোঝবার সুবিধার্থে গানটি আর তার পরিচয় প্রথমেই দিয়ে নিই —</p>

  <p>আমি যখন তাঁর দুয়ারে ভিক্ষা নিতে যাই<br>
  তখন যাহা পাই<br>
  সে যে আমি হারাই বারে বারে।।</p>

  <p>তিনি যখন ভিক্ষা নিতে আসেন আমার দ্বারে<br>
  বন্ধ তালা ভেঙে দেখি আপন মাঝে গোপন রতনভার,,<br>
  হারায় না সে আর।।</p>

  <p>প্রভাত আসে তাঁহার কাছে আলোক ভিক্ষা নিতে,<br>
  সে আলো তার লুটায় ধরণীতে।<br>
  তিনি যখন সন্ধ্যা-কাছে দাঁড়ান ঊর্ধ্ব-করে<br>
  তখন স্তরে স্তরে<br>
  ফুটে ওঠে অন্ধকারের আপন প্রাণের ধন —<br>
  মুকুটে তাঁর পরেন সে রতন।।</p>

  <p>গানের পরিচিতি দেখছি, পর্যায়- পুজা/বিশ্ব, রাগ/তাল- পিলু/ কাহারবা ! প্রথম খটকা- পুজো তা তো সবাই দেখতে পাচ্ছে, কিন্তু “বিশ্ব”? সে আবার কোথা থেকে এল? এবার বাকি পরিচিতি- রচনা কাল ১৭ পৌষ ১৩২৪ (1st. January, 1918), কবির বয়স- ৫৬, ৫৭-এর মাঝামাঝি। এই বয়সটা, আর সেই সময়ে কবির জীবন এক সন্ধি-ক্ষণে গিয়ে দাঁড়ায়। পাঠক, সেও অনুধাবনীয়! কিন্তু সে বিষয়ে পরে আসছি। আগে দেখি পুজোর আড়ালে বিশ্বর অংশ কারা? কি তাদের পুজো, কিই বা তাদের চাওয়া-পাওয়া!</p>

  <p>প্রথম থেকে যদি বোঝবার চেষ্টা করি, তবে সহজ-ভাবে বুঝি- ‘তিনি’ নিশ্চয় ঈশ্বর, আর তাঁর কাছে ভিক্ষা নিতে যায় ‘মানব’ ‘আমি’। আর তাঁর দান সেই ‘ভিক্ষা’ সেই ‘মানব’ ‘আপনি’ হারান ‘বারে বারে’। এই রে! কি বেয়াক্কেলে লোক মশায় এই ‘মানব আমি’! ‘তাঁর’ ‘ভিক্ষার’ মতো মহামূল্যবান ‘জিনিস’, দুয়ারে দাঁড়িয়ে সাধনা করে যা পাওয়া, ‘তা’ ‘হারিয়ে’ ফেলেন ‘বারে বারে’! একি অর্বাচীন, অলপ্পেয়ে কাণ্ডকারখানা! এ কি হতে পারে? আবার এমন কি অবস্থা হয়ে দাঁড়ায় যে ‘তাঁকেই’ আবার ‘ভিক্ষে’ নিতে আসতে হয় ‘আমি মানবের’ কাছে? নাঃ! এ ঠিক খাপে খাপে বসছে না। পাঠক/শ্রোতা, বড় সহজ নয়! আমার ইচ্ছে হয় কবিকে তাঁর কবিতাই শুনিয়ে দিই– “তোমারে পাছে সহজে বুঝি, তাই কি এত লীলার ছল’?</p>

  <p>আসলে এই রহস্য বুঝতে গেলে আমাদের রূপক টাকে বিশদে বুঝতে হবে। মনে করুন পর্যায় বিভাজনে ‘বিশ্ব’ শব্দটি। আর সে-ভাবে ভাবলেই দেখি এমন ঘটনা তো আদিকাল থেকে ঘটে যাচ্ছে এই মহাবিশ্বে। প্রথমেই খুঁজে দেখি কি ‘ভিক্ষে’ চাওয়া-পাওয়া হচ্ছে। তাহলেই চরিত্র ‘আমি’, ‘তিনি’ আর ‘প্রভাত’, ‘’সন্ধ্যা’ সব প্রত্যক্ষ হয়ে উঠবে। আর আবার আবিষ্কার করব রবিকবির সেই পুরনো খেলা, রহস্যের চাবিকাঠি লুকিয়ে রাখেন গানের/কবিতার পরবর্তী অংশে। দেখুন দ্বিতীয় স্তবকে- “’প্রভাত’ আসে ‘তাঁহার’ কাছে ‘আলোক’ ‘ভিক্ষা’ নিতে”। এবার বুঝে নিন যে, সেই মহার্ঘ ভিক্ষালব্ধ জিনিসটি হচ্ছে ‘আলো’। আর তাহলেই সেই ‘আমি’ আর ‘তিনি’ বোধের মধ্যে এসে যাবে—  ‘আমি’ হচ্ছে মহাবিশ্বের তমিস্রা, রাত্রি, মহানিশা; ধরিত্রী যার ধাত্রীকোলে নিশ্চিন্তশয়ান। আর ‘তিনি’ হচ্ছেন ঈশ্বরপ্রতীক, ‘সব আলোর আকর জ্যোতিষ্ক’, আলোর স্রষ্টা, মহাশূন্যের আর পদার্থের অন্তরে জমা শক্তিপুঞ্জ। সূর্যও তাঁর থেকে ‘আলো’ নেয়।।</p>

  <p>বিশ্বের রঙ্গমঞ্চে “তোমায় আমায় মিলন হবে ব’লে/ রাত্রি জাগে জগৎ লয়ে কোলে,/ ঊষা এসে পূর্বদুয়ার খোলে, কলকণ্ঠস্বরা।।“ রোজ সেই শূন্যতায় ভরা ‘অন্ধকার’, ‘আলো’ চেয়ে নিয়ে যায়, রোজ রাতে আবার সে হারায় সে ‘আলো’। অথচ এই দু‘জন না হ’লে সৃষ্টি হয় না। জগৎ তামসে ডুবে থাকে। তাই তাদের মধ্যে কাজ করে এক অমোঘ আকর্ষণ, দু’জনেই সমকক্ষ, কেউ কমবেশি নয়। ‘বালক’ ‘প্রভাত’ যে ‘আলো’ চেয়ে নিয়ে যায়, তা এই ধরণীর কোলে ফেলে, ‘লুটিয়ে দিয়ে’, সে যায় তার আপন খেলায় মাতে। বড় হয়ে দিন হবার খেলায়। আর দিনশেষে, যখন ‘সূর্য’ তার ‘আলো নিয়ে রাত্রির হাতে ধরণীর দায়িত্ব রেখে যাবার আগে দিনান্তের সীমায় এসে নামে, তখন সেই “আলোকময় সত্তা” রাত্রির কাছে ‘ভিক্ষা’ মাগেন; আপাতশূন্য তমিস্রা আবিষ্কার করে তার মধ্যেই আছে ‘গোপন রতনভার’; আঁধার আকাশে ঝকমক করে তারার সারি। ‘আলো’ হারিয়ে যায় না আর। যে ‘আলো’ ‘প্রভাত’কে‘ ভিক্ষা দিয়েছিলেন 'তিনি’, পৃথিবীতে বিলাবার জন্যে, সেই 'আলো’রই প্রত্যার্পণ চেয়ে ‘তিনি’ দাঁড়ান ‘রাত্রি’ উদ্ভিন্নযৌবনা রূপ- ‘সন্ধ্যা’র কাছে। সকালে তিনি দে’ন, আর সন্ধ্যায় তিনি চেয়ে নে’ন।</p>

  <p>আর এই দেয়া-নেয়া'র খেলায় যে রূপ সৃষ্টি হয় তার বর্ণনা পাই এই গানে-<br>
  “কালের মন্দিরা যে সদাই বাজে/<br>
  ডাইনে বাঁয়ে দুই হাতে/”<br>
  যেখানে কবি বলে ওঠেন-....<br>
  “তালে তালে সাঁঝ-সকালে রূপসাগরে ঢেউ লাগে/<br>
  সাদা-কালোর দ্বন্দ্বে যে ওই ছন্দে নানান রঙ জাগে/”।</p>

  <p>এই আলো-আঁধারির মধ্যে অমোঘ আকর্ষণের গল্প বহু প্রাচীন। আমার মনে পরে যায় চীন দেশের পুরাণের সৃষ্টি-তত্ত্বের ‘ইন’ আর ‘ইয়াং’-এর কথা। এই রূপক যেন সেই কথারই কথকতা। যাঁরা এ কথার সাথে অপরিচিত তাঁদের জন্যে বলি- চীনা পৌরাণিক কথায়, ’ইন’ এবং ‘ইয়াং’ দু’টি বিপরীত কিন্তু পরস্পর সংযুক্ত শক্তিকে প্রতিনিধিত্ব করে, যাদের প্রায়শই অন্ধকার ও আলো, নারী ও পুরুষ, অথবা নিষ্ক্রিয় ও সক্রিয় সত্তা হিসেবে বর্ণনা করা হয়, যারা মহাবিশ্বের মধ্যে ভারসাম্য ও সামঞ্জস্য বজায় রাখতে নিরন্তর ক্রিয়াশীল; এই ধারণাটি তাওবাদে গভীরভাবে প্রোথিত। এদের কালো (ইন) ও সাদা (ইয়াং) রঙের ঘূর্ণায়মান প্যাটার্নে বিভক্ত একটি বৃত্তের প্রতীকী চিহ্ন দিয়ে আঁকা হয়, যাতে প্রতিটি অংশ একে অন্যের বিপরীত রঙে (সাদা, কালো) চিত্রিত ছবি হয়ে থাকে। এই চিত্র তাদের ঘনিষ্ঠ অথচ পরস্পর না-ছোঁয়া সাহচর্য এক অপ্রতিরোধ্য আকর্ষণ অথচ স্বতন্ত্র স্বাধীন একাত্মতা দেখায়। আমাদের জীবনের অর্থ দেখায়। ‘ইন’ শব্দের আক্ষরিক অনুবাদ হল ‘ছায়াযুক্ত দিক’ বা ‘অন্ধকার দিক’, আর ‘ইয়াং’-এর অর্থ ‘সূর্যালোকিত দিক’ বা ‘উজ্জ্বল দিক’। ‘ইন’- রাত্রি, জল, চাঁদ, নারীসুলভ স্থিরতা, নিষ্ক্রিয়তা এইসবের প্রতীক আর ‘ইয়াং’- দিন, অগ্নি, সূর্য, পুরুষসুলভ, গতি, সক্রিয়তার।</p>

  <p>পুরুষ ও প্রকৃতির এই খেলাতেই এই পৃথিবী জীবনদায়ী আলোতে সমুজ্জ্বল। আবার কবির চেতনায় এই আলোর ধারা ভালোবাসার প্রতীক। “আলোকের এই ঝরণাধারায় ধুইয়ে দাও”। এই তো আকাঙ্খা ‘মানবের’। যখন বেদনার আঁধার ঘনিয়ে আসে, তখনি তাঁর প্রেমের ধারা আলোর মতো এসে আমাদের ভাসিয়ে তোলে সেই ভুবন ব্যেপে বয়ে যাওয়া আনন্দধারায়।</p>

  <p>এবার আবার যদি পঠনে আর বিশ্লেষণে ফিরে যাই তবে দেখি- বালক ‘প্রভাত,’ রাত্রি যখন যাব-যাব, -তখন এসে ‘আলো’ চেয়ে নিয়ে খেলার ছলে ঢেলে দেয় পৃথিবী-পরে। আবার যখন দিনের শেষে ‘সূর্যের আলোর’ যাবার পালা। আঁধার ছাইতে থাকে দিগ্বিদিকে। ‘আলো’ হারানোর বেদনা নামে ধরিত্রীর বুকে। আর তখন সেই ‘জ্যোতিষ্ক’ ঈশ্বর, ‘তন্দ্রালসা, সোনার আঁচল খসা’ লাস্যময়ী রাত্রির যৌবনবতী সন্ধ্যারূপের কাছে দু’হাত উঁচু করে স্তবের ভঙ্গীতে দাঁড়ান, হৃদয়ঘন সেই মুহূর্তে সেই আকাশে ফোটে পরতে পরতে সেকি রঙবাহারী রত্ন! সে ‘ভিক্ষার রতন’ তখন তাঁর মাথার মুকুটে জায়গা করে নেয়। এত অপরূপ এই ‘ইন’ আর ‘ইয়াং’-এর দেওয়া-নেওয়া!</p>

  <p>এবার বিশ্বরূপ থেকে চোখ ফেরাই কবির জীবনে। আসলে সব কবিতা, গানই তো কবির আপনমাঝে মগ্ন আত্মকথন। আগেই বলেছি এই ৫৬-৫৭ বয়েসেই প্রিয়জন হারানো প্রায় নিঃসঙ্গ কবির জীবনে কাঁপিয়ে দিয়েছে এক কিশোরীর আবির্ভাব। সে কবিকে চিঠি লিখেছে এ গানের ১০/১২ মাস আগে থেকে, ১৯১৭ সালে। তারপর এই ১৯১৮ সালের জানুয়ারীর মধ্যে সেই পত্রালাপ পৌঁছেছে কুহু-কেকা সম্বাদে। কবি তাঁর জীবনে বহুবার হারিয়েছেন প্রিয়জনদের। জীবনের প্রভাতে হেলায় না-চিনতে পারা আকর্ষণকে, তাঁর উদ্দীপনদাত্রী ‘শ্রীমতী হেকেট, বা শুধু ‘শ্রীমতী হে’ (Greek goddess Hecate)- কে হারিয়েছেন তাঁর স্বেচ্ছায় জগত থেকে বিদায় নেওয়ায়। তখন থেকে তাঁর একাকী সময়ে যখন তাঁর আঁধার মন ঈশ্বরের কাছে আলো চান, দেখেন সেই সব সময়ের আনন্দধারা স্মৃতিরা, বিচ্ছিন্ন বেদনার আঁধার আকাশে তারার মতো জ্বলছে। তারা তাঁর লেখায় ফুটে উঠেছে, উঠছে। হারিয়ে যাচ্ছে না। আর বিস্ময়করভাবে, এই জীবনপ্রদোষে আবার যেন নবরূপে ফিরে এসেছে ‘সে’, এই ৫৬-৫৭ বছর বয়েসে, সন্ধ্যাকাশে লেগেছে রঙের মেলা। তারা প্রোথিত হয়ে যাচ্ছে তাঁর লেখায়, গান হয়ে ফুটে উঠছে একে একে। অভিভূত কবি – তাঁর গভীর প্রাণের আনন্দ এই গান, যাতে তাঁর জীবনের ছন্দ মিলেমিশে একাকার। এইভাবেই এই গান ছুঁয়ে যায় আমাদের। স্তব্ধ, আনত হই পূজার স্তবে, তাঁর সাথে ভাগ করে নিতে এই প্রাপ্তির মুগ্ধতাকে।</p>


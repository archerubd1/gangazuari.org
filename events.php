<!-- Custom Toggle Script -->
<script>
document.querySelectorAll('[data-toggle="tab"]').forEach(tab => {
  tab.addEventListener('click', function (e) {
    e.preventDefault();
    const targetId = this.getAttribute('href');
    const target = document.querySelector(targetId);

    // If already active, toggle it off
    if (this.classList.contains('active')) {
      this.classList.remove('active', 'btn-primary');
      this.classList.add('btn-outline-primary');
      target.classList.remove('show', 'active');
    } else {
      // Reset all tabs
      document.querySelectorAll('[data-toggle="tab"]').forEach(el => {
        el.classList.remove('active', 'btn-primary');
        el.classList.add('btn-outline-primary');
      });
      document.querySelectorAll('.tab-pane').forEach(pane => {
        pane.classList.remove('show', 'active');
      });

      // Activate this tab
      this.classList.add('active', 'btn-primary');
      this.classList.remove('btn-outline-primary');
      target.classList.add('show', 'active');
    }
  });
});
</script>







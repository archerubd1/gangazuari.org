  <h2 class="mb-3"> অরিজিৎ কথঞ্চিৎ</h2>
  <h5 class="mb-4">বহু বিদ্যা, তীক্ষ্ণ বোধ, তথাপি নির্বোধ- ১<br>
নির্বুদ্ধিতা- ব্যক্তিগত বনাম গোষ্ঠীগত</h5>
 <p style="text-align: center;">
  এক বিষয়ের পণ্ডিত যিনি, তীক্ষ্ণ বুদ্ধিমান,<br>
  অন্য বিষয় বুঝতে গিয়ে হিমশিম খান।
</p>



  <p>নির্বুদ্ধিতা- ব্যক্তিগত বনাম গোষ্ঠীগত</p>
  <p>এক বিষয়ের পণ্ডিত যিনি, তীক্ষ্ণ বুদ্ধিমান, অন্য বিষয় বুঝতে গিয়ে হিমশিম খান। মেধাবী মানুষ—যেমন কোন বিখ্যাত দার্শনিক বা বিজ্ঞানী দৈনন্দিন সাধারণ সিদ্ধান্ত নিতে ভুল করে আমাদের অবাক করেন। কুরি দম্পতি মারী ও পিয়ের প্ল্যানচেটে যান, নিউটন ইথারে বিশ্বাস করেন আর না বুঝতে পারা শেয়ার বাজারে টাকা খোওয়ান, আইনস্টাইন অনিশ্চয়তা তত্ত্বের বিরোধিতা করতে গিয়ে ঈশ্বরেচ্ছার শরণ নেন। সেই স্বাভাবিক ঘটনাবলী দেখে অন্যরা যে অবাক হন তার কারণ বিভা-প্রভাব (Halo effect)। কেউ একটি বিষয়ে ভাল করলে অন্য বিষয়েও উৎকৃষ্ট হবেন—এই ভুল ধারণা। যেমন, পদার্থবিদ্যায় পণ্ডিত শেয়ার বাজারেও ভাল করবেন—এমনটা নয়।</p>

  <p>আমরা ভুলে যাই, অতি বিখ্যাত পদার্থবিদ রিচার্ড ফাইনম্যান বলেছেন, নিজের বিষয়ের বাইরে বিজ্ঞানীরা এক একজন সাধারণ মানুষ। তবু, বিখ্যাত মানুষদের যতক্ষণ ভুল আর ঝুঁকি ব্যক্তির সীমার মধ্যে থাকে, বৃহত্তর জনসমাজে তেমন কিছু হয় না। দেশেরও আসে যায় না। কিন্তু বৃহত্তর সমাজ যদি তাঁদের ঈশ্বরের আসনে বসিয়ে অনুকরণ করতে শুরু করে, তখন বিপদের সূচনা হয়। যেমন—‘মহম্মদ ইউনুস ব্যাঙ্ক চালাতে দক্ষ, অতএব উনি দেশও ভালই চালাবেন।’ বাংলাদেশ এই বিভা-প্রভাবিত ভাবনার মুল্য দিয়ে চলেছে।</p>

  <p>নির্বোধদের কেউ কেউ ব্যক্তিগতভাবে নিজ নিজ পেশায় অতি উৎকৃষ্টও হতে পারেন। তবে সাধারণ নির্বোধদের সঙ্গে তাঁদের অন্তরের মিল সীমিত পরিমণ্ডলের বাইরে নিজের বিচারবুদ্ধি বিসর্জন দেওয়ায়। সময়সাপেক্ষ বাস্তব সমাধানের চেয়ে এঁরা সহজ, চটজলদি উপায় চান। হিটলারের ‘ইহুদি নিকেশ করলে আমরাই সবার ওপরে উঠে পৃথিবীতে রাজত্ব করব’—এই সহজ সমাধান শিক্ষিত-অশিক্ষিত নির্বিশেষে জার্মানরা বিশ্বাস করে নিজেদের ধ্বংস ডেকে এনেছিল। একইভাবে এই উপমহাদেশে জনসাধারণও সহজ সমাধান আঁকড়ে ধরে—যেমন, ‘গোবর মেখে স্নান করলে কোভিড ছুঁতে পারবে না’, অথবা ‘কোরান পড়লে গণিতে জ্ঞানোদয় হবে’।</p>

  <h3 class="mb-3 mt-5">সহজের ফাঁদ</h3>
  <p>জটিল দেখলে পালাতে চাও, নেই কো তাতে দোষ,<br>
  সহজ শুধু ফেললে ফাঁদে, মনে রেখো না রোষ।।</p>

  <p>মানুষের সমস্যাগুলো যেমন—শিক্ষা, খাদ্য, কাজ, স্বাস্থ্য—জটিল, সমাধানও সহজ নয়। প্রাজ্ঞ মানুষ সমস্যার গভীরে চিন্তা করেন, চটজলদি সমাধানের প্রতিশ্রুতি দিতে পারেন না। বারট্রান্ড রাসেলের ভাষায়—‘নিজেদের মত ও পথ সম্বন্ধে নির্বোধ আর উগ্র মতাবলম্বীরা সর্বদাই ঘোরতর আত্মবিশ্বাসী, আর বিবেচক ও প্রাজ্ঞ মানুষেরা সর্বদাই সন্দেহাকুল।’</p>

  <p>কিন্তু বুদ্ধিমানের ভেকধারী ধূর্ত সহজ ও দ্রুত সমাধানের প্রতিশ্রুতি দেন। ক্লান্ত মানুষ সেটাই চায়। যেমন—বন্ধু বললো, ‘পড়াশোনা শক্ত লাগছে? একটু গাঁজা টেনে নিলে সহজে বুঝতে পারবি।’ কিংবা সিনেমায় ভিলেনকে খতম করলে দর্শক নিশ্চিন্ত হয়। গুরু বলেন, ‘তোকে আমি রক্ষা করব’—মানুষ তাঁর চরণে আশ্রয় নেয়। রাজনীতিক বলেন, ‘আমাকে ভোট দিলেই সব ঠিক করে দেব’—মানুষ তাকেও বিশ্বাস করে। এই সহজ সমাধানের অমোঘ টান রাজনীতিতে ধূর্তদের সংখ্যা বাড়ায়।</p>

  <h3 class="mb-3 mt-5">সামাজিক হওয়া ভালো, তার মধ্যেও কালো</h3>
  <p>মানুষ দাঁত-নখ-পেশীতে দুর্বল প্রাণী। কিন্তু দল বেঁধে বেঁচে থাকার প্রবণতা তাকে টিকিয়ে রেখেছে। চেতনার গভীরে থাকা সেই ভীতি মাঝে মাঝে বড় ক্ষতির কারণ হয়। ১৯৫১ সালে সলোমন অ্যাশ ১২৩ জন ছাত্রকে নিয়ে দেখান, সংখ্যাগরিষ্ঠের সাথে থাকার জন্য মানুষ অনেক সময় জেনেশুনেই ভুল উত্তর দেয়। মানুষ সামাজিক জীব—ভীতি ও একাকীত্ব তাকে দলের আশ্রয়ে ঠেলে দেয়।</p>

  <p>নিজের সুবিধার জন্য একা হওয়া যেমন বিপদ, আবার নানা মত না এলে সর্বোত্তম সমাধানও পাওয়া যায় না। তাই সামাজিকতার অন্ধ আনুগত্য পশ্চাদপদতার জন্ম দেয়।</p>

  <h3 class="mb-3 mt-5">ডিট্রিষ বনহোফার ও নির্বুদ্ধিতার ভয়</h3>
  <p>‘আমরাই শ্রেষ্ঠ, ইহুদি মেরে বিশ্ব জয় করব’—এই কথায় শিক্ষিত জার্মানরা কীভাবে বিশ্বাস করলো? এই প্রশ্নে নাৎসি-বিরোধী জার্মান পাদ্রী ডিট্রিষ বনহোফার (Dietrich Bonhoeffer) বিব্রত হয়েছিলেন। শেষপর্যন্ত হিটলারের মৃত্যুর তিন সপ্তাহ আগে তাঁকে ফাঁসি দেওয়া হয় (৯ এপ্রিল, ১৯৪৫, বয়স মাত্র ৩৯ বছর)।</p>

  <p>বনহোফার জানতেন—মানুষ ভালমন্দ বিচার করতে পারে। কিন্তু গোষ্ঠীগত নির্বুদ্ধিতা মানুষের বুদ্ধি কেড়ে নেয়। কোভিডের সময় থালা বাজানো, আফগানিস্তানে মেয়েদের পড়াশোনা বন্ধ করা—সবই এই প্রবণতার উদাহরণ। বনহোফারের মতে, নির্বোধ হওয়া একটি মানসিক অবস্থা—যে কোন স্তরের বুদ্ধিমানও আক্রান্ত হতে পারেন।</p>

  <p>বন্ধুদের উদ্দেশে তাঁর শেষ চিঠিতে বনহোফার লেখেন, নির্বোধরা বিশ্বাস করেন তাঁরা মহৎ কাজ করছেন—তাই তাঁদের বিরোধিতা বিপদের কারণ। এজন্যই আফগানিস্তান বা ইরানে নারীর স্বাধীনতা প্রাণঘাতী হয়ে ওঠে।</p>

  <h3 class="mb-3 mt-5">বাহ্যিক শক্তির প্রভাব</h3>
  <p>ব্যক্তিগত নির্বুদ্ধিতা বড় শক্তির সহায়তা না পেলে সামাজিক স্তরে ছড়ায় না। ক্ষমতাসীনরা মদত দিলে নির্বুদ্ধিতা অতিমারির মত ছড়ায়। যুক্তি ও বিশ্লেষণ সরিয়ে স্লোগান ও ব্যঙ্গোক্তি দখল করে নেয় মানুষের চিন্তা। এর ফলে প্রাজ্ঞতাকে তুচ্ছ করে নির্বুদ্ধিতা উন্মাদনায় রূপ নেয়।</p>

  <p>উদাহরণস্বরূপ—অষ্টম থেকে ত্রয়োদশ শতকে আরব মনীষারা জ্ঞান-বিজ্ঞানে অগ্রগণ্য ছিলেন। কিন্তু উগ্র ইসলামী শক্তির উত্থান তাদের মেধাকে ডুবিয়েছে।</p>

  <h3 class="mb-3 mt-5">ব্যক্তি মনের ছবি</h3>
  <p>নির্বোধরা খারাপ কাজ করেও খারাপ বোঝে না। নাৎসি অফিসার অ্যাডলফ আইখমান নিজের দায়িত্ব পালন করেছে বলেই মনে করতেন। হানা আরেন্ড তাঁর বিচার দেখে বলেন—তিনি ‘ভয়ংকরভাবে সাধারণ’। চিন্তার অক্ষমতা তাঁকে নিষ্ঠুরতা বোঝাতেই দেয়নি।</p>

  <p>ভালোর জন্য চুরি করি, বলুক লোকে চোর,<br>
  তাদের ভালো করেই আমি যাবো জীবনভোর।</p>

  <p>এই গ্লানিহীনতা মানুষকে এমন স্তরে নিয়ে যায় যেখানে গণহত্যা, শিশুহত্যা, ধর্ষণও ভয়ঙ্কর মনে হয় না। বনহোফার তাই বলেন—‘নির্বুদ্ধিতা শয়তানির চেয়েও ভয়ঙ্কর।’</p>

